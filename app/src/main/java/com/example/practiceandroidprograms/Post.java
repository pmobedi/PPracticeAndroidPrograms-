package com.example.practiceandroidprograms;

public class Post {
    private int userId;
    private int id;
    private String title;
    private String body;

    // Getter و Setter برای userId
    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    // Getter و Setter برای id
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    // Getter و Setter برای title
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    // Getter و Setter برای body
    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
}
